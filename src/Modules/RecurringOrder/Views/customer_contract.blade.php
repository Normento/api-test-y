<h4>
    PREAMBULE:
</h4>
<p>
    <strong>Mr/Mme</strong> {{ $user->first_name }} {{ $user->last_name }} a souhaité recruter l’Entreprise YLOMI
    spécialisée dans l’Entretien et le Nettoyage, le recrutement, la formation et la gestion de personnel pour les
