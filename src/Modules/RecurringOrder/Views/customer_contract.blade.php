    déterminée.
</p>

<p>
    <strong>Article 1 :</strong> Objet
</p>
<p>Le présent contrat est un contrat de prestation de service ayant pour objet, le recrutement, la formation, la mise à
    disposition et la gestion de personnels pour le compte du client..
</p>
<p>Le client peut modifier la nature des prestations à tout moment. Elle devra notifier ce changement au prestataire
    (15) jours avant son entrée en vigueur. Un tel changement pourrait affecter ou non les autres clauses du présent
    contrat.
</p>
<br>
<p>
    <strong>Article 2 :</strong> Nombre de personnels
</p>
<p>
    <strong>Ylomi</strong> s’engage à recruter et mettre à la disposition du client sous gestion <strong>
        {{ count($acceptedPropositions) }}
        prestataires </strong>qualifiés et dignes de confiance. Le nombre peut varier à tout moment selon les
    besoins du client sur le terrain. Une liste de prestataires recrutés et mise à disposition sera annexée à ce contrat
    et pourra être actualisée à tout moment selon l’évolution des besoins du client.

</p>
<br>
<p>
    <strong>Article 3 :</strong> Calendrier de travail
</p>
<p>Le personnel mise à disposition du client est en service (fréquence d’intervention) <strong> pause y compris sans
        dépasser
        quarante (40) heures de travail par semaine</strong> conformément aux dispositions particulières du client.
</p>
<br>
<p>
    <strong>Article 4 :</strong> Rémunération
</p>
<p>
    Le client devra rémunérer le prestataire pour les prestations, par versement mensuel de <strong>{{
        $budgetInLetter }} soit ({{ $total_budget }}) .</strong> francs CFA
    hors TVA avant le cinq (05) de
    chaque mois sur présentation d’une facture normalisée. Sont acceptés le virement sur compte bancaire numéro
    <strong>504090007151</strong> de l’Entreprise YLOMI auprès de la banque <strong>UBA</strong> ou le paiement par
    chèque ou encore via Mobile money dans l’application Ylomi.
</p>
<br>
<p>
    <strong>Article 5 :</strong> Durée
</p>
<p>
    Ce contrat est conclu pour une durée déterminée <strong>d’un (01) an renouvelable par tacite reconduction</strong>.
    Il entre en
    vigueur à la date du (date de déploiement) avec une période d’essai de trois (03) mois, période au cours de laquelle
    les deux (02) parties peuvent rompre le contrat sans conditions et sans indemnités.
</p>
<br>
<p>
    <strong>Article 6 :</strong> Obligations et responsabilités du prestataire
</p>
<p>
    <strong>6.1</strong> Le prestataire s’engage alors à assurer lui-même l’exécution des prestataires et à ne pas les
    sous-traiter.

    <strong>6.2</strong> Le prestataire exécutera sa prestation dans le strict respect du règlement intérieur du client.

    <strong>6.3</strong> Le prestataire s’engage à faire toutes les formalités administratives nécessaires à l’endroit
    de ou des employés
    et prendre en charge la gestion de tous les conflits administratifs qui pourraient naître pendant et après la
    prestation.

    <strong>6.4</strong> Le prestataire s’engage à procéder au remplacement systématique de ou des employés en cas de
    défaillance ou
    d’imprévu contre paiement de FCFA 10 000 pour frais de formalités de recrutement. Ces frais sont applicables
    uniquement pour les employés recrutés par les soins du client et qui doivent être remplacés par Ylomi.

    <strong>6.5</strong> Le prestataire s’engage à recycler le ou les employés si leurs performances ne sont pas à la
    hauteur des
    attentes du client afin d’améliorer de façon continue la qualité de la prestation.

    <strong>6.6</strong> Le prestataire s’engage à céder au client qui exprime la demande de gestion d’employé que
    lui-même à recruter et
    mise à disposition du client contre une indemnisation d’un mois de prestation avec un minimum de FCFA 50 000 si le
    montant de la prestation par employé est inférieur à ce montant.
</p>
<br>
<p>
    <strong>Article 7 :</strong> Obligations et responsabilités du client
</p>
<p>
    <strong>7.1</strong> Le client s’engage à réserver aux employés un accès facile au lieu du travail et des conditions
    de travail humaines et conviviales tout en évitant TOUS FAITS DE MALTRAITANCE PHYSIQUE OU MORALE conformément à
    l’article 5 de l’arrêté N°026/MFPTRA/DC/DT/SRT du 14 avril 1998.

    <strong>7.2</strong> Le client s’engage à ne pas avoir des comportements de débauchage des employés recrutés par les
    soins du prestataire en respectant les procédures de fonctionnement du prestataire sous peine d’une amande de FCFA
    200 000. De même, en cas de rupture de contrat, le client s’interdit à recruter l’employé préalablement recruté par
    Ylomi avant une période de deux (02) mois sous peine d’une amande de FCFA 500 000.

    <strong>7.3</strong> Le client s’engage à signaler à temps au prestataire tout mauvais comportement des
    prestataires/employés afin de permettre au prestataire de prendre des dispositions idoines.

    <strong>7.4</strong> Le client s’engage à payer la facture du prestataire à bonne date et exclusivement au
    prestataire pour éviter tout conflit.

    <strong>7.5</strong> Le client s’engage à utiliser le ou les employés de Ylomi exclusivement pour le poste ou les
    tâches auxquels ils ont été recrutés. En tout état de cause, Ylomi ne sera pas responsable des déconvenues qui
    surviendraient du changement ou de l’ajout de tâches non convenus.

    <strong>7.6</strong> Le client s’engage à n’octroyer aucun prêt ou avance à l’employé sans le consentement préalable
    du prestataire. En tout état de cause, Ylomi ne sera en aucun cas responsable de non remboursement d’un prêt auquel
    elle n’a pas été associé
</p>
<br>
<p>
    <strong>Article 8 :</strong> Modifications – Nullité partielle – Notifications
</p>
<p>
    <strong>8.1</strong> Le contrat ne peut être modifié que par accord écrit et exprès des parties. Toute modification
    sera formalisée dans un avenant écrit, dument signé par les parties. Cet avenant sera alors considéré comme formant
    partie du contrat.

    <strong>8.2</strong>Toutes notifications entre les deux parties seront faites par voie électronique professionnelle
    infos@ylomi.net pour Ylomi (Adresse mail du client) pour le client avec accusé de réception sous 72heures.

</p>
<br>
<p>
    <strong>Article 9 :</strong> Résiliation anticipée du contrat
</p>
<p>
    En cas de violation par l’une des parties de l’une quelconque des clauses du contrat, il est expressément convenu
    que quinze (15) jours ouvrables après une simple mise en demeure adressée par courrier électronique professionnel
    aux adresses citées au 8.2, demeuré sans effet après 72 heures et mentionnant l’intention de résilier le contrat, la
    partie non fautive aura le droit de résilier de plein droit le contrat aux torts et griefs de la partie défaillante
    sans préjudice de tous dommages et intérêts qu’elle pourrait réclamer à la partie défaillante.
</p>
<br>
<p>
    <strong>Article 10 :</strong> Référencement
</p>
<p>
    Le client accepte de figurer parmi les références des travaux accomplis de YLOMI dans le cadre du présent contrat.
</p>
<br>
<p>
    <strong>Article 11 :</strong> Loi applicable Attribution de compétence
</p>
<p>
    Tout litige concernant l’interprétation, la validité, l’exécution du contrat et/ou des opérations qui en seront la
    suite et/ou la conséquence sera en premier lieu, résolues par voie de conciliation d’un (01) mois, et en cas
    d’échec, soumis à la compétence exclusive des tribunaux compétents du ressort de la Cour d’appel de Cotonou qui
    seront appelés à statuer suivant le droit Béninois.
</p>
